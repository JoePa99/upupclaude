interface FunnelChartPreviewProps {
  content: any;
}

function FunnelChartPreview({ content }: FunnelChartPreviewProps) {
  const stages = content.stages || [];

  return (
    <motion.div
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      className="space-y-6"
    >
      <div className="bg-white/60 backdrop-blur-xl border border-white/80 rounded-3xl p-8 shadow-luminous">
        <h2 className="text-2xl font-extrabold tracking-tight text-luminous-text-primary mb-8">
          Funnel Analysis
        </h2>

        <div className="space-y-4">
          {stages.map((stage: any, i: number) => {
            const maxCount = stages[0]?.count || 1;
            const widthPercent = (stage.count / maxCount) * 100;

            return (
              <div key={i}>
                <div className="flex items-center justify-between mb-2">
                  <span className="font-bold text-luminous-text-primary">{stage.name}</span>
                  <span className="text-sm text-luminous-text-secondary font-medium">
                    {stage.count.toLocaleString()} ({stage.conversion_rate}%)
                  </span>
                </div>
                <div className="h-12 bg-luminous-text-tertiary/10 rounded-2xl overflow-hidden">
                  <motion.div
                    className="h-full bg-gradient-to-r from-luminous-accent-cyan to-luminous-accent-purple flex items-center justify-center text-white font-bold"
                    initial={{ width: 0 }}
                    animate={{ width: `${widthPercent}%` }}
                    transition={{ duration: 0.8, delay: i * 0.1 }}
                  >
                    {widthPercent > 20 && `${stage.conversion_rate}%`}
                  </motion.div>
                </div>
              </div>
            );
          })}
        </div>
      </div>
    </motion.div>
  );
}
