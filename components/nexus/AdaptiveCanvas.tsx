'use client';

import { motion, AnimatePresence } from 'framer-motion';
import type { Message } from '@/types';

interface AdaptiveCanvasProps {
  isOpen: boolean;
  message: Message | null;
  onClose: () => void;
}

/**
 * NEXUS Adaptive Canvas - Slide-out panel for rich artifacts
 * Renders structured JSON outputs as interactive UI
 */
export function AdaptiveCanvas({ isOpen, message, onClose }: AdaptiveCanvasProps) {
  if (!message?.metadata?.artifact_type) return null;

  const artifactType = message.metadata.artifact_type;
  const content = message.metadata.content;

  return (
    <AnimatePresence>
      {isOpen && (
        <>
          {/* Backdrop */}
          <motion.div
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            exit={{ opacity: 0 }}
            onClick={onClose}
            className="fixed inset-0 bg-black/20 backdrop-blur-sm z-40"
          />

          {/* Canvas Panel */}
          <motion.div
            initial={{ x: '100%', opacity: 0 }}
            animate={{ x: 0, opacity: 1 }}
            exit={{ x: '100%', opacity: 0 }}
            transition={{ type: 'spring', stiffness: 300, damping: 30 }}
            className="fixed right-0 top-0 h-screen w-[40%] bg-white/80 backdrop-blur-2xl border-l border-white/90 shadow-2xl z-50 flex flex-col"
          >
            {/* Header */}
            <div className="p-6 border-b border-luminous-text-tertiary/10 flex items-center justify-between">
              <div>
                <h2 className="text-2xl font-extrabold tracking-tight text-luminous-text-primary">
                  {artifactType === 'linkedin_post' ? 'LinkedIn Post' :
                   artifactType === 'report' ? 'Report' :
                   artifactType === 'chart' ? 'Chart' : 'Artifact'}
                </h2>
                <p className="text-sm text-luminous-text-secondary font-medium mt-1">
                  Generated by {message.author.name}
                </p>
              </div>
              <motion.button
                onClick={onClose}
                className="w-10 h-10 rounded-full bg-luminous-text-tertiary/10 hover:bg-luminous-text-tertiary/20 flex items-center justify-center transition-colors"
                whileHover={{ scale: 1.1 }}
                whileTap={{ scale: 0.9 }}
              >
                <svg width="16" height="16" viewBox="0 0 16 16" fill="none" xmlns="http://www.w3.org/2000/svg">
                  <path d="M12 4L4 12M4 4L12 12" stroke="currentColor" strokeWidth="2" strokeLinecap="round"/>
                </svg>
              </motion.button>
            </div>

            {/* Content Area */}
            <div className="flex-1 overflow-y-auto p-6">
              {artifactType === 'linkedin_post' && <LinkedInPostPreview content={content} />}
              {artifactType === 'report' && <ReportPreview content={content} />}
              {artifactType === 'chart' && <ChartPreview content={content} />}
              {artifactType === 'funnel_analysis' && <FunnelChartPreview content={content} />}
            </div>

            {/* Footer Actions */}
            <div className="p-6 border-t border-luminous-text-tertiary/10 flex items-center gap-3">
              <motion.button
                className="flex-1 px-4 py-3 rounded-2xl bg-luminous-accent-purple text-white font-bold shadow-luminous"
                whileHover={{ scale: 1.02, y: -2 }}
                whileTap={{ scale: 0.98 }}
              >
                Export
              </motion.button>
              <motion.button
                className="flex-1 px-4 py-3 rounded-2xl bg-white/60 backdrop-blur-xl border border-white/80 text-luminous-text-primary font-bold"
                whileHover={{ scale: 1.02, y: -2 }}
                whileTap={{ scale: 0.98 }}
              >
                Edit
              </motion.button>
            </div>
          </motion.div>
        </>
      )}
    </AnimatePresence>
  );
}

